"""Маша составляет коды из букв, входящих в слово ДОБРЫНЯ. Каждая буква должна входить в код
ровно один раз. Все возможные коды Маша записывает в алфавитном порядке и нумерует.
Начало списка выглядит так:
1 БДНОРЫЯ
2 БДНОРЯЫ
3 БДНОЫРЯ
...
Какой код будет записан под номером 3377?"""
import itertools
alphabet = "ДОБРЫНЯ"
ar = itertools.permutations(alphabet)
arl = []
for e in ar:
    arl.append(list(e))
print(*arl[265])
